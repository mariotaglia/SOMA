static char *package_name = 0;

/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given,
               char *value, const char *possible_values[],
               const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option, const char *long_opt, char short_opt, const char *additional_error)
{
    char *stop_char = 0;
    const char *val = value;
    int found;
    char **string_field;
    FIX_UNUSED(field);

    stop_char = 0;
    found = 0;

    if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
        {
            if (short_opt != '-')
                fprintf(stderr, "%s: `--%s' (`-%c') option given more than once%s\n",
                        package_name, long_opt, short_opt, (additional_error ? additional_error : ""));
            else
                fprintf(stderr, "%s: `--%s' option given more than once%s\n",
                        package_name, long_opt, (additional_error ? additional_error : ""));
            return 1;           /* failure */
        }

    if (possible_values && (found = check_possible_values((value ? value : default_value), possible_values)) < 0)
        {
            if (short_opt != '-')
                fprintf(stderr, "%s: %s argument, \"%s\", for option `--%s' (`-%c')%s\n",
                        package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt, short_opt,
                        (additional_error ? additional_error : ""));
            else
                fprintf(stderr, "%s: %s argument, \"%s\", for option `--%s'%s\n",
                        package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt,
                        (additional_error ? additional_error : ""));
            return 1;           /* failure */
        }

    if (field_given && *field_given && !override)
        return 0;
    if (prev_given)
        (*prev_given)++;
    if (field_given)
        (*field_given)++;
    if (possible_values)
        val = possible_values[found];

    switch (arg_type)
        {
        case ARG_FLAG:
            *((int *)field) = !*((int *)field);
            break;
        case ARG_INT:
            if (val)
                *((int *)field) = strtol(val, &stop_char, 0);
            break;
        case ARG_DOUBLE:
            if (val)
                *((double *)field) = strtod(val, &stop_char);
            break;
        case ARG_ENUM:
            if (val)
                *((int *)field) = found;
            break;
        case ARG_STRING:
            if (val)
                {
                    string_field = (char **)field;
                    if (!no_free && *string_field)
                        free(*string_field);    /* free previous string */
                    *string_field = gengetopt_strdup(val);
                }
            break;
        default:
            break;
        };

    /* check numeric conversion */
    switch (arg_type)
        {
        case ARG_INT:
        case ARG_DOUBLE:
            if (val && !(stop_char && *stop_char == '\0'))
                {
                    fprintf(stderr, "%s: invalid numeric value: %s\n", package_name, val);
                    return 1;   /* failure */
                }
            break;
        default:
            ;
        };

    /* store the original value */
    switch (arg_type)
        {
        case ARG_NO:
        case ARG_FLAG:
            break;
        default:
            if (value && orig_field)
                {
                    if (no_free)
                        {
                            *orig_field = value;
                        }
                    else
                        {
                            if (*orig_field)
                                free(*orig_field);      /* free previous string */
                            *orig_field = gengetopt_strdup(value);
                        }
                }
        };

    return 0;                   /* OK */
}

int
cmdline_parser_internal(int argc, char **argv, struct som_args *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
    int c;                      /* Character of the parsed option.  */

    int error_occurred = 0;
    struct som_args local_args_info;

    int override;
    int initialize;
    int check_required;
    int check_ambiguity;

    package_name = argv[0];

    override = params->override;
    initialize = params->initialize;
    check_required = params->check_required;
    check_ambiguity = params->check_ambiguity;

    if (initialize)
        cmdline_parser_init(args_info);

    cmdline_parser_init(&local_args_info);

    optarg = 0;
    optind = 0;
    opterr = params->print_errors;
    optopt = '?';

    while (1)
        {
            int option_index = 0;

            static struct option long_options[] = {
                {"help", 0, NULL, 'h'},
                {"detailed-help", 0, NULL, 0},
                {"version", 0, NULL, 'V'},
                {"coord-file", 1, NULL, 'c'},
                {"timesteps", 1, NULL, 't'},
                {"ana-file", 1, NULL, 'a'},
                {"gpus", 1, NULL, 'g'},
                {"only-gpu", 1, NULL, 'o'},
                {"screen-output-interval", 1, NULL, 's'},
                {"rng-seed", 1, NULL, 'r'},
                {"pseudo-random-number-generator", 1, NULL, 'p'},
                {"omp-threads", 1, NULL, 'n'},
                {"nonexact-area51", 0, NULL, 0},
                {"move-type", 1, NULL, 0},
                {"iteration-alg", 1, NULL, 0},
                {"skip-tests", 0, NULL, 0},
                {"load-balance", 1, NULL, 'l'},
                {"accepted-load-inbalance", 1, NULL, 0},
                {"autotuner-restart-period", 1, NULL, 0},
                {"N-domains", 1, NULL, 'd'},
                {"domain-buffer", 1, NULL, 0},
                {"rcm-update", 1, NULL, 0},
                {"user", 1, NULL, 0},
                {"set-generation-algorithm", 1, NULL, 0},
                {"bond-minimum-image-convention", 0, NULL, 'm'},
                {"no-sync-signal", 0, NULL, 0},
                {"long-chain-threshold", 1, NULL, 0},
                {"set_order_frequency", 1, NULL, 0},
                {"n_random_q", 1, NULL, 0},
                {"final-file", 1, NULL, 'f'},
                {0, 0, 0, 0}
            };

            c = getopt_long(argc, argv, "hVc:t:a:g:o:s:r:p:n:l:d:mf:", long_options, &option_index);

            if (c == -1)
                break;          /* Exit from `while (1)' loop.  */

            switch (c)
                {
                case 'h':      /* Print help and exit.  */
                    cmdline_parser_print_help();
                    cmdline_parser_free(&local_args_info);
                    return (1);

                case 'V':      /* Print version and exit.  */
                    cmdline_parser_print_version();
                    cmdline_parser_free(&local_args_info);
                    return (1);

                case 'c':      /* File containing the system description and all Coordinates. (HDF5-Format).  */

                    if (update_arg((void *)&(args_info->coord_file_arg),
                                   &(args_info->coord_file_orig), &(args_info->coord_file_given),
                                   &(local_args_info.coord_file_given), optarg, 0, 0, ARG_STRING,
                                   check_ambiguity, override, 0, 0, "coord-file", 'c', additional_error))
                        goto failure;

                    break;
                case 't':      /* Number of MC sweeps carried out by SOMA..  */

                    if (update_arg((void *)&(args_info->timesteps_arg),
                                   &(args_info->timesteps_orig), &(args_info->timesteps_given),
                                   &(local_args_info.timesteps_given), optarg, 0, 0, ARG_INT,
                                   check_ambiguity, override, 0, 0, "timesteps", 't', additional_error))
                        goto failure;

                    break;
                case 'a':      /* File containing the analysis frequency and is going to be appended by the new measured observables. (HDF5-Format).  */

                    if (update_arg((void *)&(args_info->ana_file_arg),
                                   &(args_info->ana_file_orig), &(args_info->ana_file_given),
                                   &(local_args_info.ana_file_given), optarg, 0, "", ARG_STRING,
                                   check_ambiguity, override, 0, 0, "ana-file", 'a', additional_error))
                        goto failure;

                    break;
                case 'g':      /* Number of GPUs per MPI-node to use. The devices 0-(gpus-1) are going to be occupied by SOMA. Every node must feature this number of nodes and it is assumed that the scheduler assignes the ranks consequently to the nodes. If set to 0, SOMA tries to run on host-code. Ignored if compiled without OPENACC..  */

                    if (update_arg((void *)&(args_info->gpus_arg),
                                   &(args_info->gpus_orig), &(args_info->gpus_given),
                                   &(local_args_info.gpus_given), optarg, 0, "0", ARG_INT,
                                   check_ambiguity, override, 0, 0, "gpus", 'g', additional_error))
                        goto failure;

                    break;
                case 'o':      /* Specify a specific Device for all ranks. Useful for MPI-single rank runs. This option overrides the --gpus option..  */

                    if (update_arg((void *)&(args_info->only_gpu_arg),
                                   &(args_info->only_gpu_orig), &(args_info->only_gpu_given),
                                   &(local_args_info.only_gpu_given), optarg, 0, 0, ARG_INT,
                                   check_ambiguity, override, 0, 0, "only-gpu", 'o', additional_error))
                        goto failure;

                    break;
                case 's':      /* Specify the number of seconds between an output about timings on the screen..  */

                    if (update_arg((void *)&(args_info->screen_output_interval_arg),
                                   &(args_info->screen_output_interval_orig),
                                   &(args_info->screen_output_interval_given),
                                   &(local_args_info.screen_output_interval_given), optarg, 0, "10", ARG_DOUBLE,
                                   check_ambiguity, override, 0, 0, "screen-output-interval", 's', additional_error))
                        goto failure;

                    break;
                case 'r':      /* Global seed for the pseudo-random number generator. If you pass seed < 0, seed = time(NULL) will be used. Option useful for debuggin purposes..  */

                    if (update_arg((void *)&(args_info->rng_seed_arg),
                                   &(args_info->rng_seed_orig), &(args_info->rng_seed_given),
                                   &(local_args_info.rng_seed_given), optarg, 0, "-1", ARG_INT,
                                   check_ambiguity, override, 0, 0, "rng-seed", 'r', additional_error))
                        goto failure;

                    break;
                case 'p':      /* Option to select the pseudo random number generator..  */

                    if (update_arg((void *)&(args_info->pseudo_random_number_generator_arg),
                                   &(args_info->pseudo_random_number_generator_orig),
                                   &(args_info->pseudo_random_number_generator_given),
                                   &(local_args_info.pseudo_random_number_generator_given), optarg,
                                   cmdline_parser_pseudo_random_number_generator_values, "PCG32", ARG_ENUM,
                                   check_ambiguity, override, 0, 0, "pseudo-random-number-generator", 'p',
                                   additional_error))
                        goto failure;

                    break;
                case 'n':      /* Number of omp threads used per MPI rank. If you pass n < 1 it will be set to 1..  */

                    if (update_arg((void *)&(args_info->omp_threads_arg),
                                   &(args_info->omp_threads_orig), &(args_info->omp_threads_given),
                                   &(local_args_info.omp_threads_given), optarg, 0, "1", ARG_INT,
                                   check_ambiguity, override, 0, 0, "omp-threads", 'n', additional_error))
                        goto failure;

                    break;
                case 'l':      /* Frequency of the load balancer. For homogenous architectures this can be set to high values, for hetereogenous architectures across the MPI ranks small values help to equilibrate faster. Non-MPI runs are uneffected. Values < 0 deactivate the load-balancer..  */

                    if (update_arg((void *)&(args_info->load_balance_arg),
                                   &(args_info->load_balance_orig), &(args_info->load_balance_given),
                                   &(local_args_info.load_balance_given), optarg, 0, "500", ARG_INT,
                                   check_ambiguity, override, 0, 0, "load-balance", 'l', additional_error))
                        goto failure;

                    break;
                case 'd':      /* Number of domains for a domain decomposition. (Domain decomposition is only made linear along the X-Axes).  */

                    if (update_arg((void *)&(args_info->N_domains_arg),
                                   &(args_info->N_domains_orig), &(args_info->N_domains_given),
                                   &(local_args_info.N_domains_given), optarg, 0, "1", ARG_INT,
                                   check_ambiguity, override, 0, 0, "N-domains", 'd', additional_error))
                        goto failure;

                    break;
                case 'm':      /* Specify the bond length used to calculate the energy. This decides, whether the bond length between two particle is calculated as the absolute distance or the minimum image distance..  */

                    if (update_arg
                        ((void *)&(args_info->bond_minimum_image_convention_flag), 0,
                         &(args_info->bond_minimum_image_convention_given),
                         &(local_args_info.bond_minimum_image_convention_given), optarg, 0, 0, ARG_FLAG,
                         check_ambiguity, override, 1, 0, "bond-minimum-image-convention", 'm', additional_error))
                        goto failure;

                    break;
                case 'f':      /* Filename to write the final configuration. (HDF5-Format).  */

                    if (update_arg((void *)&(args_info->final_file_arg),
                                   &(args_info->final_file_orig), &(args_info->final_file_given),
                                   &(local_args_info.final_file_given), optarg, 0, "end.h5", ARG_STRING,
                                   check_ambiguity, override, 0, 0, "final-file", 'f', additional_error))
                        goto failure;

                    break;

                case 0:        /* Long option with no short option */
                    if (strcmp(long_options[option_index].name, "detailed-help") == 0)
                        {
                            cmdline_parser_print_detailed_help();
                            cmdline_parser_free(&local_args_info);
                            return (1);
                        }

                    /* Specify to use the exact check of area51. This includes checks, whether a particle moves through a forbidden area51. Performance might be slightly increased if switched to nonexact. Configuration generation is always in exact mode..  */
                    if (strcmp(long_options[option_index].name, "nonexact-area51") == 0)
                        {

                            if (update_arg
                                ((void *)&(args_info->nonexact_area51_flag), 0, &(args_info->nonexact_area51_given),
                                 &(local_args_info.nonexact_area51_given), optarg, 0, 0, ARG_FLAG, check_ambiguity,
                                 override, 1, 0, "nonexact-area51", '-', additional_error))
                                goto failure;

                        }
                    /* Specify the Monte-Carlo move type..  */
                    else if (strcmp(long_options[option_index].name, "move-type") == 0)
                        {

                            if (update_arg((void *)&(args_info->move_type_arg),
                                           &(args_info->move_type_orig), &(args_info->move_type_given),
                                           &(local_args_info.move_type_given), optarg, cmdline_parser_move_type_values,
                                           "SMART", ARG_ENUM, check_ambiguity, override, 0, 0, "move-type", '-',
                                           additional_error))
                                goto failure;

                        }
                    /* Specify the iteration algorithm of the beads. This specifies also the level of parallelism that is possible..  */
                    else if (strcmp(long_options[option_index].name, "iteration-alg") == 0)
                        {

                            if (update_arg((void *)&(args_info->iteration_alg_arg),
                                           &(args_info->iteration_alg_orig), &(args_info->iteration_alg_given),
                                           &(local_args_info.iteration_alg_given), optarg,
                                           cmdline_parser_iteration_alg_values, "POLYMER", ARG_ENUM, check_ambiguity,
                                           override, 0, 0, "iteration-alg", '-', additional_error))
                                goto failure;

                        }
                    /* Skip tests SOMA is usually preforming before and after the simulation to ensure integrety of the data..  */
                    else if (strcmp(long_options[option_index].name, "skip-tests") == 0)
                        {

                            if (update_arg((void *)&(args_info->skip_tests_flag), 0, &(args_info->skip_tests_given),
                                           &(local_args_info.skip_tests_given), optarg, 0, 0, ARG_FLAG,
                                           check_ambiguity, override, 1, 0, "skip-tests", '-', additional_error))
                                goto failure;

                        }
                    /*  [0,100] Percent of step time which is ignored by load balancer. Low values enable better load balancing, but could cause fluctuation of polymers..  */
                    else if (strcmp(long_options[option_index].name, "accepted-load-inbalance") == 0)
                        {

                            if (update_arg((void *)&(args_info->accepted_load_inbalance_arg),
                                           &(args_info->accepted_load_inbalance_orig),
                                           &(args_info->accepted_load_inbalance_given),
                                           &(local_args_info.accepted_load_inbalance_given), optarg, 0, "8", ARG_DOUBLE,
                                           check_ambiguity, override, 0, 0, "accepted-load-inbalance", '-',
                                           additional_error))
                                goto failure;

                        }
                    /* Period in which the autotuner is restarted..  */
                    else if (strcmp(long_options[option_index].name, "autotuner-restart-period") == 0)
                        {

                            if (update_arg((void *)&(args_info->autotuner_restart_period_arg),
                                           &(args_info->autotuner_restart_period_orig),
                                           &(args_info->autotuner_restart_period_given),
                                           &(local_args_info.autotuner_restart_period_given), optarg, 0, "10000",
                                           ARG_INT, check_ambiguity, override, 0, 0, "autotuner-restart-period", '-',
                                           additional_error))
                                goto failure;

                        }
                    /* Number of buffer cells which can contain ghost particles. Experiment and find the optimum for your simulation..  */
                    else if (strcmp(long_options[option_index].name, "domain-buffer") == 0)
                        {

                            if (update_arg((void *)&(args_info->domain_buffer_arg),
                                           &(args_info->domain_buffer_orig), &(args_info->domain_buffer_given),
                                           &(local_args_info.domain_buffer_given), optarg, 0, "10", ARG_INT,
                                           check_ambiguity, override, 0, 0, "domain-buffer", '-', additional_error))
                                goto failure;

                        }
                    /* Frequency of the update of the molecule center of mass. If they enter the ghost layer they are sen. Experiment and find the optimum for your simulation.  */
                    else if (strcmp(long_options[option_index].name, "rcm-update") == 0)
                        {

                            if (update_arg((void *)&(args_info->rcm_update_arg),
                                           &(args_info->rcm_update_orig), &(args_info->rcm_update_given),
                                           &(local_args_info.rcm_update_given), optarg, 0, "10", ARG_INT,
                                           check_ambiguity, override, 0, 0, "rcm-update", '-', additional_error))
                                goto failure;

                        }
                    /* Additional arguments. The usage of these arguments defined by the user. The default setting ignores the arguments..  */
                    else if (strcmp(long_options[option_index].name, "user") == 0)
                        {

                            if (update_arg((void *)&(args_info->user_arg),
                                           &(args_info->user_orig), &(args_info->user_given),
                                           &(local_args_info.user_given), optarg, 0, 0, ARG_STRING,
                                           check_ambiguity, override, 0, 0, "user", '-', additional_error))
                                goto failure;

                        }
                    /* Option to select the algorithm to generate the indepent sets..  */
                    else if (strcmp(long_options[option_index].name, "set-generation-algorithm") == 0)
                        {

                            if (update_arg((void *)&(args_info->set_generation_algorithm_arg),
                                           &(args_info->set_generation_algorithm_orig),
                                           &(args_info->set_generation_algorithm_given),
                                           &(local_args_info.set_generation_algorithm_given), optarg,
                                           cmdline_parser_set_generation_algorithm_values, "SIMPLE", ARG_ENUM,
                                           check_ambiguity, override, 0, 0, "set-generation-algorithm", '-',
                                           additional_error))
                                goto failure;

                        }
                    /* Synchronize MPI ranks for correct signal catching. OFF enables termination via sending SIGINT or SIGTERM to SOMA if the MPI library supports it. ON accerlerates run with many MPI ranks..  */
                    else if (strcmp(long_options[option_index].name, "no-sync-signal") == 0)
                        {

                            if (update_arg
                                ((void *)&(args_info->no_sync_signal_flag), 0, &(args_info->no_sync_signal_given),
                                 &(local_args_info.no_sync_signal_given), optarg, 0, 0, ARG_FLAG, check_ambiguity,
                                 override, 1, 0, "no-sync-signal", '-', additional_error))
                                goto failure;

                        }
                    /* Option to determine the length of the long chain, expressed as the inverse fraction of the total bead number..  */
                    else if (strcmp(long_options[option_index].name, "long-chain-threshold") == 0)
                        {

                            if (update_arg((void *)&(args_info->long_chain_threshold_arg),
                                           &(args_info->long_chain_threshold_orig),
                                           &(args_info->long_chain_threshold_given),
                                           &(local_args_info.long_chain_threshold_given), optarg, 0, "50", ARG_INT,
                                           check_ambiguity, override, 0, 0, "long-chain-threshold", '-',
                                           additional_error))
                                goto failure;

                        }
                    /* Option to determine the frequency to check the ordering of the polymers which is important for independent_set_iteration..  */
                    else if (strcmp(long_options[option_index].name, "set_order_frequency") == 0)
                        {

                            if (update_arg((void *)&(args_info->set_order_frequency_arg),
                                           &(args_info->set_order_frequency_orig),
                                           &(args_info->set_order_frequency_given),
                                           &(local_args_info.set_order_frequency_given), optarg, 0, "200", ARG_INT,
                                           check_ambiguity, override, 0, 0, "set_order_frequency", '-',
                                           additional_error))
                                goto failure;

                        }
                    /* Option to determine the number of random wave vectors used for the calculation of structure factor..  */
                    else if (strcmp(long_options[option_index].name, "n_random_q") == 0)
                        {

                            if (update_arg((void *)&(args_info->n_random_q_arg),
                                           &(args_info->n_random_q_orig), &(args_info->n_random_q_given),
                                           &(local_args_info.n_random_q_given), optarg, 0, "32", ARG_INT,
                                           check_ambiguity, override, 0, 0, "n_random_q", '-', additional_error))
                                goto failure;

                        }

                    break;
                case '?':      /* Invalid option.  */
                    /* `getopt_long' already printed an error message.  */
                    goto failure;

                default:       /* bug: option not considered.  */
                    fprintf(stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c,
                            (additional_error ? additional_error : ""));
                    abort();
                }               /* switch */
        }                       /* while */

    if (check_required)
        {
            error_occurred += cmdline_parser_required2(args_info, argv[0], additional_error);
        }

    cmdline_parser_release(&local_args_info);

    if (error_occurred)
        return (-1);

    return 0;

 failure:

    cmdline_parser_release(&local_args_info);
    return (-1);
}
